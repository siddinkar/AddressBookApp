class Contact {
  constructor(id, name, phone, email, address, isFavorite, image, notes) {
    this.id = id;
    this.name = name;
    this.phone = phone;
    this.email = email;
    this.address = address;
    this.isFavorite = isFavorite;
    this.image = image;
    this.notes = notes;
  }
}

export default Contact;
